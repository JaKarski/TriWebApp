				<p>Pływanie</p>
				Jeżeli temperatura wody w zbiorniku wodnym nie przekracza 28,5 stopnia Celsjusza, zawodnik może użyć kombinezonu pływackiego ze specjalnej pianki. Zawodnicy pływają między oznaczonymi bojami. Niedozwolony jest wszelki sprzęt pływacki – np. płetwy, rurki do nurkowania – można korzystać jedynie z gogli pływackich. Zawodnik może w każdej chwili zatrzymać się w wodzie w pozycji stojącej (jeśli zbiornik jest na tyle płytki) bądź złapać się boi. Nie może za to się od niej odpychać. Zawodnik może być eskortowany, a w razie potrzeby może być mu udzielana pomoc, zapobiegająca utonięciu. Niedozwolona jest pomoc eskorty w ukończeniu etapu.
				Po wyjściu z wody zawodnik przemieszcza się do strefy zmian, w której zmienia się czepek na kask rowerowy oraz nakłada się obuwie, które zazwyczaj jest przytwierdzone do pedałów.
				<p>Jazda na rowerze</p>
				Każdy zawodnik jest zobowiązany do posiadania kasku. Podczas jazdy zabronione jest ściąganie kasku. Uczestnik zawodów może go zdjąć po zejściu z roweru np. w celu jego poprawienia. Zawodnik może prowadzić lub nieść rower (np. w przypadku awarii), jednak nie może ukończyć etapu bez niego. Zawodnicy nie mogą używać sprzętu redukującego opór powietrza. Zabroniony jest draft, czyli jazda w grupie na dystansach 113 km (70,3) i 226 km (IM). Niedozwolony jest montaż kołpaków.
				Zawodnik, który kończy etap rowerowy, znajduje się w strefie zmian (T2), gdzie zostawia kask i zmienia obuwie na biegowe.
				<p>Bieg</p>
				Uczestnik może pokonać ostatni dystans dowolnym sposobem – biegiem zarówno do przodu, jak i do tyłu, dozwolony jest także marsz. Zawodnik nie może korzystać z pomocy z zewnątrz (oprócz wolontariuszy, którzy rozstawieni na trasie podają płyny oraz żywność), biec z nagim torsem, czołgać się, biec bez butów w jakiejkolwiek części trasy biegowej, biec z kaskiem na głowie, używać przedmiotów stałych do zmiany kierunku, biec w asyście pomocników, używać nielegalnego sprzętu.
